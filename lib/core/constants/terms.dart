2.2. Қосымшаны пайдалана отырып, Қосымшада тіркелуден/авторизациядан өтіп, ақпараттық қызметтер көрсетуге тапсырыс ресімдей отырып және/немесе Шарт жасаса отырып, Сіз ерікті түрде, өз еркіңізбен және өз мүддеңіз үшін Дербес ақпаратты өңдеудің мынадай тәсілдеріне жазбаша келісім бересіз: жазу, жүйелеу, сақтау, нақтылау (жаңарту, өзгерту), шығарып алу, пайдалану, үшінші тұлғаларға беру (ұсыну, қолжеткізу), иесіздендіру, бұғаттау, жою, жойып жіберу – осы Саясатта белгіленген мақсаттар мен тәртіпте, автоматтандыру құралдарын пайдалану арқылы немесе онсыз, Құқық иеленушінің қалауы бойынша.
</p>

<p>
2.3. Осы Саясатқа, оның ережелерін түсіндіруді және қабылдау, орындау, өзгерту және тоқтату тәртібін қоса алғанда, Қазақстан Республикасының заңнамасы қолданылады.
</p>

<h2>3. Дербес ақпарат</h2>

<p>
3.1. Осы Саясатта дербес ақпарат деп мыналар түсініледі:
</p>

<p>
3.1.1. Пайдаланушы Қосымшада тіркелу немесе авторизациялау кезінде, сондай-ақ Сервисті өзге түрде пайдалану барысында өзі туралы дербес ұсынатын ақпарат, соның ішінде Пайдаланушының дербес деректері.
</p>

<p>
3.1.2. Пайдаланушының бағдарламалық жасақтамасы параметрлеріне байланысты автоматты режимде иесіздендірілген түрде берілетін деректер.
</p>

<p>
3.2. Құқық иеленуші Сервисті пайдалану және/немесе Шарт жасасу үшін міндетті түрде ұсынылуы тиіс Пайдаланушының Дербес ақпаратының құрамына талаптар белгілеуге құқылы, бұл ақпаратты арнайы көрсету арқылы айқындалады. Егер белгілі бір ақпарат Құқық иеленуші тарапынан міндетті деп белгіленбесе, оны ұсыну немесе ашу Пайдаланушының өз қалауымен жүзеге асырылады.
</p>

<p>
3.3. Қосымшада тіркелу кезінде Пайдаланушы міндетті түрде логин мен парольді көрсетеді. Қосымша түрде электрондық пошта мекенжайы көрсетілуі мүмкін. Қосымшада тіркелу/авторизациялау Google және Apple аутентификация қызметтері арқылы жүзеге асырылуы мүмкін. Бұл жағдайда Құқық иеленушіге осындай аутентификация қызметі арқылы электрондық пошта мекенжайы беріледі.
</p>

<p>
Сервисті пайдалану кезінде Пайдаланушы Қосымша құралдары арқылы Құқық иеленушіге шығыстар мен кірістер туралы деректерді ұсынады, сондай-ақ Қосымша Пайдаланушы құрылғысының локализациясын (тіл және ел) белгілейді, ал Пайдаланушы Қосымша арқылы техникалық қолдау шеңберінде Құқық иеленушіге сұрау жолдаған кезде қосымша ретінде: Пайдаланушы құрылғысының моделі, операциялық жүйе нұсқасы, мобильді байланыс операторы беріледі.
</p>

<p>
Пайдаланушы Қосымшаны банкпен синхрондау үшін пайдаланатын ешқандай авторизация деректері Құқық иеленушіге берілмейді, бұл деректердің барлығы тек Қосымшаның данасы орнатылған Пайдаланушы құрылғысында ғана сақталады.
</p>

<p>
3.4. Жалпыға қолжетімді ақпарат.
</p>

<p>
Тіркелу кезінде, сондай-ақ Пайдаланушы Сервистегі «пікірлер» және «комментарийлер» бөлімдерінде пікір немесе ұсыныс қалдырған кезде, шектеусіз тұлғалар тобына – Интернет желісі пайдаланушыларына және/немесе басқа Пайдаланушыларға Сервистегі Пайдаланушы профилінің деректері: аты, тегі және фотосуреті (аватар) қолжетімді болуы мүмкін.
</p>

<p>
Пайдаланушы көрсетілген деректерге шектеусіз тұлғалар тобының қол жеткізуіне хабардар түрде келісім береді, мұндай деректер Пайдаланушы Сервисте комментарийді өзі орналастырған сәттен бастап немесе оның өтініші бойынша орналастырылған сәттен бастап жалпыға қолжетімді болады.
</p>

<p>
3.5. Құқық иеленуші ұсынылатын Дербес ақпараттың дұрыстығын және Пайдаланушыда осы Саясатқа сәйкес оны өңдеуге қажетті келісімнің болуын тексермейді, себебі Пайдаланушы адал, сақтықпен әрекет етеді және мұндай ақпаратты өзекті күйде ұстау мен дербес деректер субъектілерінің барлық қажетті келісімдерін алу үшін барлық қажетті шараларды қабылдайды деп болжайды.
</p>
<p>
3.6. Пайдаланушы Құқық иеленушінің Қосымшада үшінші тұлғалардың бағдарламалық жасақтамасын қолданатынын түсінеді, қабылдайды және соған келіседі, соның нәтижесінде мұндай тұлғалар автоматты режимде иесіздендірілген деректерді ала алады және бере алады.
</p>

<p>
Көрсетілген үшінші тұлғалардың бағдарламалық жасақтамасына Қазақстан Республикасының заңнамасына қайшы келмейтін барлық қолданыстағы қосымшалар жатады.
</p>

<p>
Үшінші тұлғалардың бағдарламалық жасақтамасын пайдалану арқылы жиналатын деректерге мыналар кіруі мүмкін:
</p>

<ul>
  <li>Пайдаланушы құрылғысы туралы деректер (модель, өндіруші, бос жады көлемі), орналасқан жері (ел, қала), операциялық жүйе деректері (түрі, нұсқасы, экран ажыратымдылығы) және басқалар;</li>
  <li>сұрау деректері (уақыты, өту көзі, IP-мекенжай);</li>
  <li>Мобильді қосымшаны орнату және пайдалану туралы деректер;</li>
  <li>Қосымшадағы Пайдаланушы әрекеттері туралы өзге иесіздендірілген деректер, оның ішінде cookie арқылы алынатын деректер.</li>
</ul>

<p>
Көрсетілген бағдарламалық жасақтама құқық иеленушілері жинайтын иесіздендірілген деректердің құрамы, сондай-ақ оларды жинау және пайдалану шарттары тікелей осындай құқық иеленушілермен айқындалады және олардың Интернет желісіндегі сайттарында орналастырылған және/немесе қолжетімді құжаттармен реттеледі.
</p>

<p>
Осы Саясатты қабылдай отырып, сондай-ақ өзінің бағдарламалық жасақтамасында, оның ішінде құрылғының операциялық жүйесінде белгілі бір баптауларды орната отырып, Пайдаланушы жоғарыда көрсетілген бағдарламалық жасақтама құқық иеленушілерінің деректерді жинау және пайдалану шарттарымен келіседі.
</p>

<p>
Иесіздендірілген деректер 3.1.1-тармақта көрсетілген Пайдаланушы туралы ақпаратпен біріктірілмейді және Пайдаланушыларды сәйкестендіру үшін пайдаланылмайды.
</p>

<p>
3.7. Құқық иеленуші және үшінші тұлғалардың бағдарламалық жасақтамасы cookie файлдарын пайдаланады — бұл компьютеріңізге (браузерге) берілетін және сіздің қалауларыңыз бен Сервисті пайдалануыңыз туралы ақпаратты қамтитын мәтіннің шағын бөлігі.
</p>

<p>
Cookie файлдары сіз таңдаған белгілі бір параметрлерді, оның ішінде Қосымшадағы авторизацияны есте сақтауға көмектеседі. Cookie файлдарымен жұмыс параметрлері браузердің өзінде бапталады. Барлық cookie файлдарын қабылдауға тыйым салған жағдайда, Сіз Сервистің функционалдық мүмкіндіктерін пайдалана алмайсыз.
</p>

<h2>4. Дербес ақпаратты өңдеу мақсаттары</h2>

<p>
4.1. Құқық иеленуші Пайдаланушымен Шарттарды жасасу және орындау үшін қажетті Дербес ақпаратты ғана, соның ішінде жинауды және сақтауды қоса алғанда, өңдейді.
</p>

<p>
4.2. Құқық иеленуші Дербес ақпаратты мынадай мақсаттарда пайдалануға құқылы:
</p>

<p>
4.2.1 Сервисті пайдалану туралы Келісімді жасасу, сондай-ақ Сервисті пайдалана отырып өзге Шарттарды жасасу.
</p>

<p>
Шарттарды жасасу қарапайым электрондық қолтаңбамен қол қойылатын электрондық құжаттармен алмасу арқылы қашықтықтан жүзеге асырылады. Бұл ретте Пайдаланушының қарапайым электрондық қолтаңбасы кілтінің рөлін логин-пароль жұбы атқарады. Сондықтан олар тіркелу кезінде сұратылады.
</p>

<p>
4.2.2. Пайдаланушымен жасалған Шарттар бойынша міндеттемелерді орындау шеңберінде Пайдаланушыны сәйкестендіру.
</p>

<p>
Пайдаланушының Жеке кабинеті Пайдаланушының логин-паролімен және электрондық пошта мекенжайымен байланыстырылған.
</p>

<p>
Тапсырыс туралы ақпаратты жіберу, ақпараттық және техникалық қолдау көрсету кезінде Пайдаланушы логин, аты немесе электрондық пошта мекенжайы бойынша сәйкестендіріледі.
</p>

<p>
4.2.3. Жасалған Келісім және өзге Шарттар бойынша міндеттемелерді орындау, оның ішінде Пайдаланушыға Сервиске қолжеткізуді және техникалық қолдауды ұсыну, Пайдаланушының Сервистің функционалдық мүмкіндіктерін пайдалануы.
</p>

<p>
Сервистің тиісті функционалдық мүмкіндіктерін іске асыру мақсатында Құқық иеленуші Пайдаланушылар профильдерін, сондай-ақ Пайдаланушы ұсынған өзге ақпаратты Қосымшада сақтайды, жүйелейді және көрсетеді.
</p>

<p>
Техникалық қолдау көрсету үшін Пайдаланушы құрылғысының локализациясы (тіл және ел), құрылғы моделі, операциялық жүйе нұсқасы, мобильді байланыс операторы туралы деректер пайдаланылады.
</p>

<p>
4.2.4. Пайдаланушының Дербес ақпаратының қауіпсіздігі мен құпиялылығын қамтамасыз ету.
</p>

<p>
Қосымшадағы күмәнді әрекеттерді тексеру мақсатында Пайдаланушы пайдаланатын IP-мекенжай мен браузер туралы ақпарат жиналады.
</p>

<p>
4.2.5. Сервис шеңберінде ақпараттық қызмет көрсету және жасалған Келісім мен өзге шарттар бойынша Сервис сапасын жақсарту мақсатында, оның ішінде үшінші тұлғаларды тарта отырып хабарландыру тәртібімен, Пайдаланушымен байланысты қамтамасыз ету.
</p>

<p>
Бұл үшін Пайдаланушының электрондық пошта мекенжайы сұратылады.
</p>

<p>
4.2.6. Ұсынылатын Сервис сапасын жақсарту мақсатында иесіздендірілген деректер негізінде маркетингтік, статистикалық және өзге зерттеулер жүргізу.
</p>

<p>
Саясаттың 3.6-тармағында көрсетілген аналитикалық деректерді жинау жүйелерінен алынған деректер Пайдаланушылардың қызығушылықтары мен қалауларын анықтау үшін иесіздендірілген түрде жиналып, талданады.
</p>

<p>
4.2.7. Саясаттың 3.6-тармағында көрсетілген деректерді жинау жүйелерінен алынған иесіздендірілген деректерді пайдалана отырып жарнамалық және/немесе ақпараттық материалдарды таргеттеу.
</p>

<h2>5. Дербес ақпаратты қорғауға қойылатын талаптар</h2>

<p>
5.1. Құқық иеленуші Дербес ақпаратты сақтауды жүзеге асырады және оны санкцияланбаған қолжеткізуден және таратудан ішкі қағидалар мен регламенттерге сәйкес қорғайды.
</p>

<p>
5.2. Пайдаланушының Дербес ақпаратына қатысты оның құпиялылығы сақталады, осы Саясаттың 3.4-тармағында көзделген Жалпыға қолжетімді ақпаратты қоспағанда.
</p>

<p>
5.3. Сервис сапасын арттыру мақсатында Құқық иеленуші Пайдаланушының Сервисті пайдалану шеңберінде жасаған әрекеттері туралы лог-файлдарды 1 (бір) жыл бойы сақтауға құқылы.
</p>

<h2>6. Ақпаратты беру</h2>

<p>
6.1. Құқық иеленуші Дербес ақпаратты мынадай жағдайларда үшінші тұлғаларға беруге құқылы:
</p>

<ul>
  <li>Пайдаланушы мұндай әрекеттерге келісімін білдірген, оның ішінде Пайдаланушы пайдаланатын бағдарламалық жасақтама баптауларында белгілі бір ақпаратты ұсынуды шектемейтін жағдайлар;</li>
  <li>Берілім Сервисті пайдалана отырып Шарттарды жасасу және орындау үшін талап етіледі;</li>
  <li>Берілім Пайдаланушының Қосымшаның функционалдық мүмкіндіктерін пайдалануы шеңберінде қажет;</li>
  <li>Қосымшаның функционалдық мүмкіндіктерін іске асыру үшін Қосымшада үшінші тұлғалардың бағдарламалық жасақтамасын пайдалануға байланысты;</li>
