<p><strong>Мобильді қосымша</strong> – мобильді құрылғыға орнатуға және онда пайдалануға арналған, Сервистің түрлі опцияларын пайдалануға мүмкіндік беретін ЭЕМ-ге арналған бағдарлама.</p>

<p><strong>Пайдаланушы</strong> – қолданыстағы заңнама және осы Келісім талаптарына сәйкес Құқық иеленушімен өз мүддесінде немесе өзге тұлғаның мүддесінде осы Келісімді жасасқан тұлға.</p>

<p><strong>Жеке кабинет</strong> – Пайдаланушы Қосымшада тіркеуден және/немесе авторизациядан өткеннен кейін қол жеткізетін Қосымшаның дербес бөлімі. Жеке кабинет Пайдаланушының дербес деректерін сақтау, Сервистің қолжетімді функционалдық мүмкіндіктерін қарау және басқару үшін арналған.</p>

<p>
1.2. Осы Келісімде Келісімнің 1.1-тармағында көрсетілмеген өзге терминдер мен анықтамалар пайдаланылуы мүмкін.
</p>

<p>
Мұндай жағдайда тиісті терминді түсіндіру Келісім мәтініне сәйкес жүзеге асырылады. Егер Келісім мәтінінде термин немесе анықтамаға бірмәнді түсіндірме болмаса, онда оның түсіндірмесі: бірінші кезекте – онда көрсетілген Міндетті құжаттармен, екінші кезекте – Қазақстан Республикасының заңнамасымен, әрі қарай – іскерлік айналым әдет-ғұрыптарымен және ғылыми доктринамен айқындалады.
</p>

<p>
1.3. Сіздің Сервисті оның жарияланған функционалдық мүмкіндіктері шегінде кез келген тәсілмен және кез келген нысанда пайдалануыңыз, оның ішінде:
</p>

<ul>
  <li>Сервисті демонстрациялық режимде пайдалану;</li>
  <li>Сервисте тіркелу және/немесе авторизациялау;</li>
  <li>Сервистің қосымша функционалдық мүмкіндіктеріне қолжеткізуді ұсынуға тапсырыс ресімдеу;</li>
  <li>Қосымшадағы кері байланыс нысандары арқылы сұрау жолдау;</li>
  <li>Сервисті өзге түрде пайдалану,</li>
</ul>

<p>
Қазақстан Республикасының заңнамасына сәйкес осы Келісімнің және онда көрсетілген Тараптар үшін міндетті құжаттардың талаптарымен шарт жасалуына әкеледі.
</p>

<p>
1.4. Сервисті пайдаланудың жоғарыда көрсетілген мүмкіндіктерінің кез келгенін пайдалану арқылы Сіз мынаны растайсыз:
</p>

<p>
Сервисті пайдалануды бастар алдында осы Келісімнің және онда көрсетілген Міндетті құжаттардың талаптарымен толық көлемде таныстыңыз.
</p>

<p>
Осы Келісімнің және онда көрсетілген Міндетті құжаттардың барлық талаптарын өз тарапыңыздан қандай да бір алып тастаусыз және шектеусіз толық көлемде қабылдайсыз әрі оларды сақтауға не Сервисті пайдалануды тоқтатуға міндеттенесіз. Егер Сіз осы Келісімнің және онда көрсетілген Міндетті құжаттардың талаптарымен келіспесеңіз немесе олардың негізінде шарт жасасуға құқығыңыз болмаса, Сіз Сервисті кез келген пайдалануды дереу тоқтатуыңыз қажет.
</p>

<p>
Келісім (оның кез келген бөлігі қоса алғанда) және/немесе онда көрсетілген Міндетті құжаттар Құқық иеленуші тарапынан қандай да бір арнайы хабарламасыз өзгертілуі мүмкін. Келісімнің және/немесе онда көрсетілген Міндетті құжаттардың жаңа редакциясы Сайтта орналастырылған сәттен бастап не Пайдаланушының назарына өзге қолайлы нысанда жеткізілген сәттен бастап күшіне енеді, егер Келісімнің және/немесе Тараптар үшін міндетті құжаттардың жаңа редакциясында өзгеше көзделмесе.
</p>

<h2>2. Сервисті пайдаланудың жалпы шарттары</h2>

<p>
2.1. Осы Келісімді жасасудың міндетті шарты – Пайдаланушының төменде белгіленген жағдайларда мынадай құжаттарда («Міндетті құжаттар») айқындалған талаптар мен ережелерді толық және сөзсіз қабылдауы және сақтауы:
</p>

<ul>
  <li>RaBay қосымшасында орналастырылған Құпиялылық саясаты, онда Пайдаланушының дербес деректерін қоса алғанда, дербес ақпаратты ұсыну және пайдалану қағидалары қамтылған.</li>
  <li>RaBay қосымшасында орналастырылған және Пайдаланушы Тестілік кезеңнен тыс Сервистің қосымша функционалдық мүмкіндіктеріне қолжеткізуді сатып алуға тапсырыс ресімдеген жағдайда Тараптар үшін міндетті құжат болып табылатын ақпараттық қызметтер көрсету қағидалары.</li>
  <li>RaBay қосымшасында орналастырылған соңғы пайдаланушымен лицензиондық келісім – Мобильді қосымшаны пайдаланудың міндетті шарттары.</li>
</ul>

<p>
2.2. Қосымшаны және/немесе оның негізінде ұсынылатын Сервисті демонстрациялық режимде пайдалану, сондай-ақ Қосымшаның жалпыға ашық бөлімдеріндегі ақпаратты шектеулі көлемде қарау Пайдаланушының алдын ала тіркелуін және/немесе авторизациядан өтуінсіз мүмкін.
</p>

<p>
Қалған бөлігінде Қосымшаның және/немесе оның негізінде ұсынылатын Сервистің функционалдық мүмкіндіктерін қолдануға Құқық иеленуші белгілеген қағидаларға сәйкес Пайдаланушы Қосымшада тіркелуден және/немесе авторизациядан өткеннен кейін жол беріледі.
</p>

<p>
2.3. Пайдаланушыдан белгілі бір әрекеттерді жасауды талап ететін Сервис функционалдық мүмкіндіктерінің тізбесі (мысалы, Құқық иеленушімен ақылы шарт жасасу) Құқық иеленушінің дара қалауы бойынша айқындалады және мезгіл-мезгіл өзгертілуі мүмкін.
</p>

<p>
2.4. Сервисті пайдаланудың техникалық, ұйымдастырушылық және коммерциялық шарттары Сайтта бөлек орналастыру арқылы немесе Пайдаланушыларды хабарландыру арқылы Пайдаланушылар назарына жеткізілуі мүмкін.
</p>

<p>
2.5. Құқық иеленуші Сервисті пайдалануға лимиттер белгілеуге және өзге техникалық шектеулер енгізуге құқылы, олар туралы ақпарат мезгіл-мезгіл Құқық иеленушінің таңдауы бойынша нысан мен тәсілде Пайдаланушылар назарына жеткізіледі.
</p>

<h2>3. Пайдаланушы кепілдіктері</h2>

<p>
Осы Келісім талаптарын қабылдай отырып, Сіз мынаны растайсыз және кепілдік бересіз:
</p>

<p>3.1. Сервисті пайдалану туралы Шартты жасасу және оны орындау үшін барлық қажетті құқықтар мен өкілеттіктерге иесіз;</p>

<p>3.2. Сервисті Сіз тек осы Келісімде рұқсат етілген мақсаттар үшін, оның ережелерін, сондай-ақ қолданылатын құқық талаптарын және жалпы қабылданған практиканы сақтай отырып пайдаланасыз;</p>

<p>3.3. Сіз Сервисті ұсынуға немесе Сервис ұсынылатын жабдықтың, желілердің немесе бағдарламалық жасақтаманың жұмысына қайшы келетін не кедергі келтіретін қандай да бір әрекеттер жасамайсыз.</p>

<h2>4. Шектеулер</h2>

<p>
Осы Келісім талаптарымен келісе отырып, Сіз мынаны түсінесіз және мойындайсыз:
</p>

<p>4.1. Тараптардың Сервисті өтеусіз негізде ұсыну жөніндегі қатынастарына тұтынушылардың құқықтарын қорғау туралы заңнама ережелері қолданылмайды.</p>

<p>
4.2. Сервис «бар қалпында» шарттарымен ұсынылады, осыған байланысты Сізге Сервис Сіздің талаптарыңызға сай келетініне; қызметтер үздіксіз, жедел, сенімді және қатесіз ұсынылатынына; Сервисті пайдалану арқылы алынуы мүмкін нәтижелер дәл және сенімді болатынына; Сервисті пайдалану арқылы алынған қандай да бір өнімнің, қызметтің және ақпараттың сапасы Сіздің күткеніңізге сай келетініне; Сервис бағдарламалық жасақтамасындағы барлық қателер түзетілетініне қандай да бір кепілдіктер берілмейді.
</p>

<p>
4.3. Сервис жаңа функционалдық мүмкіндіктермен тұрақты түрде толықтырылып және жаңартылып отыратындықтан, ұсынылатын қызметтердің нысаны мен сипаты Сізді алдын ала хабардар етпей мезгіл-мезгіл өзгеруі мүмкін. Құқық иеленуші өз қалауы бойынша барлық Пайдаланушыларға жалпы немесе жекелеген Сізге қызметтерді (немесе қызметтер шеңберіндегі жекелеген функцияларды) ұсынуды Сізді алдын ала хабардар етпей тоқтатуға (уақытша немесе түпкілікті) құқылы.
</p>

<p>
4.4. Сіздің өз бетіңізше немесе үшінші тұлғаларды тарта отырып:
</p>

<ul>
  <li>Қосымша құрамына кіретін ЭЕМ бағдарламалары мен деректер базаларын, оның ішінде олардың кез келген элементтері мен ақпараттық материалдарын иесінің алдын ала жазбаша келісімінсіз кез келген нысанда және тәсілмен көшіруге (қайта өндіруге);</li>
  <li>Қосымшаның технологиясын ашуға, эмуляциялауға, декомпиляциялауға, дизассемблирлеуге, дешифрлеуге және өзге ұқсас әрекеттер жасауға;</li>
  <li>Құқық иеленушінің алдын ала рұқсатынсыз Сервисті пайдалана отырып бағдарламалық өнімдер және/немесе сервистер жасауға құқығыңыз жоқ.</li>
</ul>

<p>
4.5. Пайдаланушыларға Қосымшаны және/немесе оның негізінде ұсынылатын Сервисті мына мақсаттарда пайдалануға тыйым салынады:
</p>

<ul>
  <li>контрафактілік материалдарды орналастыру және/немесе тарату;</li>
  <li>порнографиялық сипаттағы материалдарды орналастыру және/немесе тарату, сондай-ақ порнографияны және балалар эротикасын насихаттау, интимдік қызметтерді жарнамалау;</li>
  <li>өзге де тыйым салынған ақпаратты тарату, соның ішінде экстремистік сипаттағы материалдарды, сондай-ақ нәсілдік және ұлттық тиістілік, дін, тіл және жыныс белгілері бойынша адам құқықтары мен бостандықтарын шектеуге бағытталған, адамға қатысты зорлық-зомбылық әрекеттерін жасауға не жануарларға адамгершілікке жатпайтын қарым-қатынасқа итермелейтін, өзге де құқыққа қайшы әрекеттерді жасауға шақыратын, оның ішінде қаруды, есірткі құралдарын және олардың прекурсорларын дайындау және қолдану тәртібін түсіндіретін материалдарды және т.б.;</li>
  <li>үшінші тұлғалардың ар-намысына, қадір-қасиетіне немесе іскерлік беделіне нұқсан келтіретін жалған ақпарат тарату;</li>
  <li>өзге сайттарға сілтемелерді басым немесе айрықша түрде орналастыру немесе тарату;</li>
  <li>өзге де құқыққа қайшы әрекеттер жасау, оның ішінде құпия ақпаратқа санкцияланбаған қолжеткізу, оны тарату, компьютерлік ақпаратқа заңсыз қолжеткізу, зиянды компьютерлік бағдарламаларды пайдалану және тарату, компьютерлік ақпаратты сақтау, өңдеу немесе беру құралдарын және ақпараттық-телекоммуникациялық желілерді пайдалану қағидаларын бұзу, құмар ойындар ұйымдастыру, лотереялар және өзге ынталандыру іс-шараларын қолданыстағы заңнаманы бұза отырып өткізу.</li>
</ul>

<p>
4.7. Сервистің жұмысында қателер анықталған жағдайда бұл туралы деректемелерде көрсетілген мекенжай бойынша немесе Қосымшада қолдау қызметі үшін бөлек көрсетілген мекенжай бойынша Құқық иеленушіге хабарлаңыз.
</p>

<p>
4.8. Сервисті ұсынуға байланысты Сіздің құқықтарыңыздың және/немесе мүдделеріңіздің бұзылғаны анықталған жағдайда, бұл туралы Құқық иеленушіге хабарлауыңыз қажет. Ол үшін бұзушылық мән-жайлары толық баяндалған жазбаша хабарлама Құқық иеленушіге жіберілуі тиіс.
</p>

